package s02;

public class IntegerTest1 {
	public static void main(String[] args) {
		byte b1 = 125; // 8bit
		short s1 = 5275; // 16bit
		int i1 = 234232; // 32bit
		// 整数默认为int类型，所以long类型要以L结尾
		long l1 = 2342342344234234L; // 64bit
		
		System.out.println("b1 的值："+b1);
		System.out.println("s1 的值："+s1);
		System.out.println("i1 的值："+i1);
		System.out.println("l1 的值："+l1);
		
	}
}
